package ProgrammingChallenges;

public class Area {
	public static double areaOfCircle(double r) {
		return Math.PI*r*r;
	}
	public static double areaOfRetangle(double width,double length) {
		return width*length;
	}
	
	public static double areaOfCylinder(double r,double height) {
		return Math.PI*r*r*height;
	}

}
